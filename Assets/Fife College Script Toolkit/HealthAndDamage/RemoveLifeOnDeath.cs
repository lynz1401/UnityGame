// *********************************************************************************************************************
#region Imports
// *********************************************************************************************************************
using System.Collections;
using System.Collections.Generic;
using UnityEngine;
#endregion
// *********************************************************************************************************************


// *********************************************************************************************************************
public class RemoveLifeOnDeath : MonoBehaviour {
	// *********************************************************************************************************************


	// *****************************************************************************************************************
	#region Variables
	// *****************************************************************************************************************
	// Exposed Variables
	[Tooltip("When this health pool dies, we'll lose a life")]
	public HealthPool m_healthPool;
	[Tooltip("This is the life pool we'll lose lives from")]
	public LifePool m_lives;
	#endregion
	// *****************************************************************************************************************


	// *****************************************************************************************************************
	#region Unity Functions
	// *****************************************************************************************************************
	// Called once per frame
	void Update()
	{
		// If the health pool has died...
		if (m_healthPool.IsAlive() == false)
		{
			m_lives.LoseLife();

			// Disable self so we don't do this twice!
			enabled = false;
		}
	}
	#endregion
	// *****************************************************************************************************************


}
// *********************************************************************************************************************
